-- LENGTH FUN:-
SELECT ONAME, LENGTH(ONAME) AS ONAME_LEN FROM ORDERS;

-- UPPERCASE (U CASE )
SELECT ONAME, UPPER(ONAME) AS UPPER_NAME  FROM ORDERS;

-- LOWER CASE ( L CASE )
SELECT *, LOWER (ONAME) AS LOWERCASE FROM ORDERS;

-- power () is used to calculate power of no. --

SELECT POWER (2,4) AS POWERNO FROM DUAL;

